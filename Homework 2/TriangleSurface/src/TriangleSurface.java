//Направете програма, която приема две цели числа - съответно страната и височината на 
//триъгълник. Пресметнете лицето на дадения триъгълник и я изведете на конзолата.

import java.util.Scanner;

public class TriangleSurface {

	public static void main(String[] args) {
		int sideTri, heightTri;
		double surfaceTri;
		Scanner inputs = new Scanner(System.in, "UTF-8");
		
		System.out.print("Въведи дължина на страната: ");
		//System.out.print("Enter length of the side: ");
		sideTri=inputs.nextInt();
		
		System.out.print("Въведи дължина на височината: ");
		//System.out.print("Enter length of the height: ");
		heightTri=inputs.nextInt();
		
		surfaceTri=sideTri*heightTri*0.5;
		
		System.out.println("Лицето на триъгълника е: " + surfaceTri);
		//System.out.println("The surface of the triangle is: " + surfaceTri);
		
		inputs.close();

	}

}
